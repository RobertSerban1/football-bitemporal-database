-- 4) REGISTRATIONS (Liga 1 & Cupa) + schimbari in sezonul de iarna
DECLARE
  v_l1 NUMBER;
  v_cu NUMBER;
BEGIN
  SELECT competition_id INTO v_l1
  FROM competitions
  WHERE code='L1' AND season='2025/26' AND ROWNUM=1;

  SELECT competition_id INTO v_cu
  FROM competitions
  WHERE code='CUPA' AND season='2025/26' AND ROWNUM=1;

  -- toti -> Liga 1
  FOR r IN (
    SELECT p.player_id, ct.club_id
    FROM players p
    JOIN contracts ct ON ct.player_id = p.player_id
                     AND ct.transaction_end = TO_TIMESTAMP('9999-12-31 23:59:59','YYYY-MM-DD HH24:MI:SS')
  ) LOOP
    version_registration(r.player_id, r.club_id, v_l1, 'registered',
                         TIMESTAMP '2025-08-01 00:00:00',
                         TIMESTAMP '2026-06-01 23:59:59');
  END LOOP;

  -- subset (player_id par) -> Cupa
  FOR r IN (
    SELECT p.player_id, ct.club_id
    FROM players p
    JOIN contracts ct ON ct.player_id = p.player_id
                     AND ct.transaction_end = TO_TIMESTAMP('9999-12-31 23:59:59','YYYY-MM-DD HH24:MI:SS')
    WHERE MOD(p.player_id,2)=0
  ) LOOP
    version_registration(r.player_id, r.club_id, v_cu, 'registered',
                         TIMESTAMP '2025-09-01 00:00:00',
                         TIMESTAMP '2026-06-01 23:59:59');
  END LOOP;

  -- ~15% schimbări iarnă (deregister + re-register)
  FOR r IN (
    SELECT p.player_id, ct.club_id
    FROM players p
    JOIN contracts ct ON ct.player_id = p.player_id
                     AND ct.transaction_end = TO_TIMESTAMP('9999-12-31 23:59:59','YYYY-MM-DD HH24:MI:SS')
    WHERE MOD(p.player_id,7)=0
  ) LOOP
    version_registration(r.player_id, r.club_id, v_l1, 'deregistered',
                         TIMESTAMP '2026-02-01 00:00:00',
                         TIMESTAMP '2026-02-15 00:00:00');
