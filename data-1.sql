-------------------------------------------------------------------------------
-- SCRIPT 1 — SCHEMA BITEMPORALĂ (tabele + constrângeri + triggere TT)
-- Oracle 21c+ | folosește IDENTITY | PERIOD FOR valid_period pe toate tabelele
-------------------------------------------------------------------------------
ALTER SESSION SET NLS_TIMESTAMP_FORMAT = 'YYYY-MM-DD HH24:MI:SS';

-------------------------------------------------------------------------------
-- 0) PALETĂ COMUNĂ: câmpuri bitemporale
--    - valid_start, valid_end       -> VALID TIME (realitatea)
--    - transaction_start, _end      -> TRANSACTION TIME (când a știut sistemul)
--    - PERIOD FOR valid_period(...) -> anunță Oracle că intervalul e “temporal”
-------------------------------------------------------------------------------

-------------------------------------------------------------------------------
-- 1) CLUBS
-------------------------------------------------------------------------------
CREATE TABLE clubs (
  club_id            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name               VARCHAR2(200) NOT NULL UNIQUE,
  country            CHAR(2) NOT NULL,
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT clubs_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT clubs_ck_tx    CHECK (transaction_end > transaction_start)
);

-------------------------------------------------------------------------------
-- 2) COACHES
-------------------------------------------------------------------------------
CREATE TABLE coaches (
  coach_id           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  full_name          VARCHAR2(200) NOT NULL,
  email              VARCHAR2(200),
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT coaches_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT coaches_ck_tx    CHECK (transaction_end > transaction_start)
);

-------------------------------------------------------------------------------
-- 3) CLUB_COACHES (istoric antrenori la club)
-------------------------------------------------------------------------------
CREATE TABLE club_coaches (
  club_coach_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_id            NUMBER NOT NULL REFERENCES clubs(club_id),
  coach_id           NUMBER NOT NULL REFERENCES coaches(coach_id),
  role               VARCHAR2(50) DEFAULT 'head' NOT NULL,
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT club_coaches_ck_role  CHECK (role IN ('head','assistant','fitness')),
  CONSTRAINT club_coaches_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT club_coaches_ck_tx    CHECK (transaction_end > transaction_start)
);

-------------------------------------------------------------------------------
-- 4) PLAYERS
-------------------------------------------------------------------------------
CREATE TABLE players (
  player_id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  full_name          VARCHAR2(200) NOT NULL,
  dob                DATE,
  nationality        CHAR(2),
  primary_position   VARCHAR2(2)
    CHECK (primary_position IN ('GK','DF','MF','FW')),
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT players_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT players_ck_tx    CHECK (transaction_end > transaction_start)
);

-------------------------------------------------------------------------------
-- 5) CONTRACTS (istoric contracte jucător–club)
-------------------------------------------------------------------------------
CREATE TABLE contracts (
  contract_id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  player_id          NUMBER NOT NULL REFERENCES players(player_id),
  club_id            NUMBER NOT NULL REFERENCES clubs(club_id),
  salary             NUMBER(12,2),
  shirt_number       NUMBER,
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT contracts_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT contracts_ck_tx    CHECK (transaction_end > transaction_start)
);

-------------------------------------------------------------------------------
-- 6) COMPETITIONS
-------------------------------------------------------------------------------
CREATE TABLE competitions (
  competition_id     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code               VARCHAR2(50) NOT NULL,
  name               VARCHAR2(200) NOT NULL,
  country            CHAR(2) NOT NULL,
  season             VARCHAR2(20) NOT NULL,
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT competitions_uq UNIQUE (code, season),
  CONSTRAINT competitions_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT competitions_ck_tx    CHECK (transaction_end > transaction_start)
);

-------------------------------------------------------------------------------
-- 7) MATCHES
-------------------------------------------------------------------------------
CREATE TABLE matches (
  match_id           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  competition_id     NUMBER NOT NULL REFERENCES competitions(competition_id),
  match_date         TIMESTAMP NOT NULL,
  home_club_id       NUMBER NOT NULL REFERENCES clubs(club_id),
  away_club_id       NUMBER NOT NULL REFERENCES clubs(club_id),
  venue              VARCHAR2(200),
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT matches_ck_teams CHECK (home_club_id <> away_club_id),
  CONSTRAINT matches_uq UNIQUE (competition_id, match_date, home_club_id, away_club_id),
  CONSTRAINT matches_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT matches_ck_tx    CHECK (transaction_end > transaction_start)
);

-------------------------------------------------------------------------------
-- 8) REGISTRATIONS (înscrieri în competiții)
-------------------------------------------------------------------------------
CREATE TABLE registrations (
  registration_id    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  player_id          NUMBER NOT NULL REFERENCES players(player_id),
  club_id            NUMBER NOT NULL REFERENCES clubs(club_id),
  competition_id     NUMBER NOT NULL REFERENCES competitions(competition_id),
  status             VARCHAR2(20)
    CHECK (status IN ('registered','loaned_out','deregistered')) NOT NULL,
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT registrations_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT registrations_ck_tx    CHECK (transaction_end > transaction_start)
);

-------------------------------------------------------------------------------
-- 9) APPEARANCES (aparitii la meci)
-------------------------------------------------------------------------------
CREATE TABLE appearances (
  appearance_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  match_id           NUMBER NOT NULL REFERENCES matches(match_id),
  player_id          NUMBER NOT NULL REFERENCES players(player_id),
  club_id            NUMBER NOT NULL REFERENCES clubs(club_id),
  role               VARCHAR2(10)
    CHECK (role IN ('starter','sub','unused')) NOT NULL,
  minutes_played     NUMBER,
  goals              NUMBER DEFAULT 0,
  assists            NUMBER DEFAULT 0,
  yellow_cards       NUMBER DEFAULT 0,
  red_cards          NUMBER DEFAULT 0,
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT appearances_ck_min   CHECK (minutes_played BETWEEN 0 AND 120),
  CONSTRAINT appearances_ck_cards CHECK (yellow_cards BETWEEN 0 AND 2 AND red_cards BETWEEN 0 AND 1),
  CONSTRAINT appearances_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT appearances_ck_tx    CHECK (transaction_end > transaction_start)
);

-------------------------------------------------------------------------------
-- 10) CLUB_DEPARTMENTS (Senior, U21 etc.)
-------------------------------------------------------------------------------
CREATE TABLE club_departments (
  department_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_id            NUMBER NOT NULL REFERENCES clubs(club_id),
  name               VARCHAR2(80) NOT NULL,
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT uq_club_dept UNIQUE (club_id, name, valid_start),
  CONSTRAINT club_dept_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT club_dept_ck_tx    CHECK (transaction_end > transaction_start)
);

-------------------------------------------------------------------------------
-- 11) COACH_DEPARTMENT_ROLES (head coach ↔️ Senior dept)
-------------------------------------------------------------------------------
CREATE TABLE coach_department_roles (
  coach_dept_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  department_id      NUMBER NOT NULL REFERENCES club_departments(department_id),
  coach_id           NUMBER NOT NULL REFERENCES coaches(coach_id),
  role               VARCHAR2(80) DEFAULT 'head' NOT NULL,
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT cdr_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT cdr_ck_tx    CHECK (transaction_end > transaction_start)
);

-------------------------------------------------------------------------------
-- 12) PLAYER_DEPARTMENT_ASSIGNMENTS (U21/Senior în timp)
-------------------------------------------------------------------------------
CREATE TABLE player_department_assignments (
  player_dept_id     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  player_id          NUMBER NOT NULL REFERENCES players(player_id),
  department_id      NUMBER NOT NULL REFERENCES club_departments(department_id),
  note               VARCHAR2(80),
  valid_start        TIMESTAMP NOT NULL,
  valid_end          TIMESTAMP NOT NULL,
  transaction_start  TIMESTAMP NOT NULL,
  transaction_end    TIMESTAMP NOT NULL,
  PERIOD FOR valid_period (valid_start, valid_end),
  CONSTRAINT pda_ck_valid CHECK (valid_end > valid_start),
  CONSTRAINT pda_ck_tx    CHECK (transaction_end > transaction_start)
);

