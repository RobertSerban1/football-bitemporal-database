-- 6) APPEARANCES (starters + subs) --
DECLARE
  CURSOR cm IS
    SELECT match_id, home_club_id, away_club_id, match_date
    FROM matches
    ORDER BY match_date, match_id;

  FUNCTION club_players_18(p_club NUMBER) RETURN SYS.ODCINUMBERLIST IS
    v SYS.ODCINUMBERLIST := SYS.ODCINUMBERLIST();
  BEGIN
    SELECT player_id BULK COLLECT INTO v
    FROM (
      SELECT p.player_id
      FROM players p
      JOIN contracts ct ON ct.player_id = p.player_id
      WHERE ct.club_id = p_club
        AND ct.transaction_end = TO_TIMESTAMP('9999-12-31 23:59:59','YYYY-MM-DD HH24:MI:SS')
      ORDER BY p.player_id
    )
    WHERE ROWNUM <= 18;
    RETURN v;
  END;
BEGIN
  FOR r IN cm LOOP
    DECLARE
      v_home SYS.ODCINUMBERLIST := club_players_18(r.home_club_id);
      v_away SYS.ODCINUMBERLIST := club_players_18(r.away_club_id);
      v_start TIMESTAMP := r.match_date;
    BEGIN
      -- home starters
      FOR i IN 1..LEAST(11, v_home.COUNT) LOOP
        version_appearance(r.match_id, v_home(i), r.home_club_id, 'starter',
                           90, CASE WHEN i<=2 THEN 1 ELSE 0 END, 0, 0, 0,
                           v_start, TO_TIMESTAMP('9999-12-31 23:59:59','YYYY-MM-DD HH24:MI:SS'));
      END LOOP;
      -- home subs
      FOR i IN 12..LEAST(18, v_home.COUNT) LOOP
        version_appearance(r.match_id, v_home(i), r.home_club_id, 'sub',
                           20, 0, 0, 0, 0,
                           v_start, TO_TIMESTAMP('9999-12-31 23:59:59','YYYY-MM-DD HH24:MI:SS'));
      END LOOP;

      -- away starters
      FOR i IN 1..LEAST(11, v_away.COUNT) LOOP
        version_appearance(r.match_id, v_away(i), r.away_club_id, 'starter',
                           90, CASE WHEN i=1 THEN 1 ELSE 0 END, 0, 0, 0,
                           v_start, TO_TIMESTAMP('9999-12-31 23:59:59','YYYY-MM-DD HH24:MI:SS'));
      END LOOP;
      -- away subs
      FOR i IN 12..LEAST(18, v_away.COUNT) LOOP
        version_appearance(r.match_id, v_away(i), r.away_club_id, 'sub',
                           15, 0, 0, 0, 0,
                           v_start, TO_TIMESTAMP('9999-12-31 23:59:59','YYYY-MM-DD HH24:MI:SS'));
      END LOOP;
    END;
  END LOOP;
END;
/
COMMIT;

