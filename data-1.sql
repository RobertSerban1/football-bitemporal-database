-- TRIGGERE: inițializează Transaction Time la INSERT (TT open-ended)
-- Rule: dacă lipsește, setăm transaction_start = SYSTIMESTAMP
--       și transaction_end   = 9999-12-31 23:59:59
-------------------------------------------------------------------------------
CREATE OR REPLACE TRIGGER set_tx_times_clubs
BEFORE INSERT ON clubs FOR EACH ROW
BEGIN
  :NEW.transaction_start := NVL(:NEW.transaction_start, SYSTIMESTAMP);
  :NEW.transaction_end   := NVL(:NEW.transaction_end,
    TO_TIMESTAMP('9999-12-31 23:59:59','YYYY-MM-DD HH24:MI:SS'));
