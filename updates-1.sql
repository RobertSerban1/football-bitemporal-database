-- Helperi
-- department_id dupa club + nume
CREATE OR REPLACE FUNCTION dept_id(p_club_name VARCHAR2, p_dept_name VARCHAR2)
RETURN NUMBER IS v_id NUMBER;
BEGIN
  SELECT d.department_id INTO v_id
  FROM club_departments d
  WHERE d.club_id = club_id(p_club_name)
    AND d.name    = p_dept_name
    AND ROWNUM=1;
  RETURN v_id;
END;
/

